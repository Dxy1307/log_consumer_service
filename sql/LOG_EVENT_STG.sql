CREATE TABLE LOG_EVENT_STG (
    ID              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    KAFKA_TOPIC     VARCHAR2(100) NOT NULL,
    KAFKA_PARTITION NUMBER NOT NULL,
    KAFKA_OFFSET    NUMBER NOT NULL,

    EVENT_TIME      TIMESTAMP NOT NULL,
    IP              VARCHAR2(50) NOT NULL,
    METHOD          VARCHAR2(10) NOT NULL,
    PATH            VARCHAR2(500) NOT NULL,
    STATUS          NUMBER NOT NULL,

    RAW_LOG         VARCHAR2(2000),

    PROCESS_STATUS  VARCHAR2(20) DEFAULT 'NEW' NOT NULL,
    RETRY_COUNT     NUMBER DEFAULT 0 NOT NULL,
    ERROR_MESSAGE   VARCHAR2(1000),

    CREATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT      TIMESTAMP
);

ALTER TABLE LOG_EVENT_STG
ADD CONSTRAINT UK_LOG_EVENT_STG_KAFKA
UNIQUE (KAFKA_TOPIC, KAFKA_PARTITION, KAFKA_OFFSET);

CREATE INDEX IDX_LOG_EVENT_STG_STATUS
ON LOG_EVENT_STG (PROCESS_STATUS, CREATED_AT);